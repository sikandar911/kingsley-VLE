import prisma from "../../config/prisma.js";

/**
 * @swagger
 * tags:
 *   name: Events
 *   description: Institution / course / section events with auto calendar reminders
 */

const eventInclude = {
  semester: { select: { id: true, name: true, year: true } },
  course: { select: { id: true, title: true } },
  section: { select: { id: true, name: true } },
  createdByUser: { select: { id: true, email: true, role: true } },
};

const parseDate = (value) => (value ? new Date(value) : null);

// ---------------------------------------------------------------------------
// helpers
// ---------------------------------------------------------------------------

/** Upserts the auto-generated CalendarReminder tied to an event. */
const upsertEventReminder = async (event, createdBy, tx = prisma) => {
  if (!event.startTime) return null;

  // Extract just the date in local time so it matches the calendar grid
  const date = new Date(event.startTime);
  date.setHours(0, 0, 0, 0);

  return tx.calendarReminder.create({
    data: {
      date,
      name: event.title,
      type: "event",
      eventId: event.id,
      courseId: event.courseId ?? null,
      sectionId: event.sectionId ?? null,
      semesterId: event.semesterId ?? null,
      createdBy,
      isAutoGenerated: true,
    },
  });
};

/** Deletes all auto-generated reminders for an event. */
const deleteEventReminders = (eventId, tx = prisma) =>
  tx.calendarReminder.deleteMany({
    where: { eventId, isAutoGenerated: true },
  });

