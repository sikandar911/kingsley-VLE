/**
 * @swagger
 * /api/events:
 *   get:
 *     summary: List events
 *     tags: [Events]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: query
 *         name: type
 *         schema: { type: string, enum: [institution, course, section] }
 *       - in: query
 *         name: courseId
 *         schema: { type: string }
 *       - in: query
 *         name: sectionId
 *         schema: { type: string }
 *       - in: query
 *         name: semesterId
 *         schema: { type: string }
 *       - in: query
 *         name: from
 *         schema: { type: string, format: date }
 *       - in: query
 *         name: to
 *         schema: { type: string, format: date }
 *       - in: query
 *         name: page
 *         schema: { type: integer, default: 1 }
 *       - in: query
 *         name: limit
 *         schema: { type: integer, default: 20 }
 *     responses:
 *       200:
 *         description: Paginated list of events
 */
export const listEvents = async (req, res) => {
  const {
    type,
    courseId,
    sectionId,
    semesterId,
    from,
    to,
    page = 1,
    limit = 20,
  } = req.query;
  const skip = (Number(page) - 1) * Number(limit);
  const where = {};

  if (type) where.type = type;
  if (courseId) where.courseId = courseId;
  if (sectionId) where.sectionId = sectionId;
  if (semesterId) where.semesterId = semesterId;
  if (from || to) {
    where.startTime = {};
    if (from) where.startTime.gte = new Date(from);
    if (to) where.startTime.lte = new Date(to);
  }

  // Students only see events scoped to their enrollments or institution-wide
  if (req.user.role === "student") {
    const studentProfile = await prisma.studentProfile.findUnique({
      where: { userId: req.user.id },
      select: {
        enrollments: { select: { courseId: true, sectionId: true } },
      },
    });

    if (studentProfile) {
      const courseIds = studentProfile.enrollments.map((e) => e.courseId);
      const sectionIds = studentProfile.enrollments
        .map((e) => e.sectionId)
        .filter(Boolean);

      where.OR = [
        { type: "institution" },
        { type: "course", courseId: { in: courseIds } },
        { type: "section", sectionId: { in: sectionIds } },
      ];
    }
  }

  try {
    const [events, total] = await Promise.all([
      prisma.event.findMany({
        where,
        skip,
        take: Number(limit),
        orderBy: { startTime: "asc" },
        include: eventInclude,
      }),
      prisma.event.count({ where }),
    ]);

    return res.json({
      data: events,
      meta: {
        total,
        page: Number(page),
        limit: Number(limit),
        pages: Math.ceil(total / Number(limit)),
      },
    });
  } catch (err) {
    console.error("listEvents error:", err);
    return res.status(500).json({ error: "Server error" });
  }
};

