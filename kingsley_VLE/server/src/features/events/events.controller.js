/**
 * @swagger
 * /api/events:
 *   post:
 *     summary: Create an event (auto-creates a calendar reminder if startTime is set)
 *     tags: [Events]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required: [type, title]
 *             properties:
 *               type:
 *                 type: string
 *                 enum: [institution, course, section]
 *               title:
 *                 type: string
 *               description:
 *                 type: string
 *               location:
 *                 type: string
 *               color:
 *                 type: string
 *                 description: Hex color code for calendar highlight (e.g. "#3B82F6")
 *               semesterId:
 *                 type: string
 *               courseId:
 *                 type: string
 *               sectionId:
 *                 type: string
 *               startTime:
 *                 type: string
 *                 format: date-time
 *               endTime:
 *                 type: string
 *                 format: date-time
 *     responses:
 *       201:
 *         description: Created event
 *       400:
 *         description: Validation error
 */
export const createEvent = async (req, res) => {
  console.log(
    "[CREATE EVENT] Received request body:",
    JSON.stringify(req.body, null, 2),
  );

  const {
    type,
    title,
    description,
    location,
    color,
    semesterId,
    courseId,
    sectionId,
    startTime,
    endTime,
  } = req.body;

  console.log("[CREATE EVENT] Parsed fields:", {
    type,
    title,
    description,
    location,
    color,
    semesterId,
    courseId,
    sectionId,
    startTime,
    endTime,
  });

  if (!type || !title) {
    return res.status(400).json({ error: "type and title are required" });
  }

  const validTypes = ["institution", "course", "section"];
  if (!validTypes.includes(type)) {
    return res
      .status(400)
      .json({ error: `type must be one of: ${validTypes.join(", ")}` });
  }

  if (type === "course" && !courseId) {
    return res
      .status(400)
      .json({ error: "courseId is required for course-type events" });
  }

  if (type === "section" && !sectionId) {
    return res
      .status(400)
      .json({ error: "sectionId is required for section-type events" });
  }

  try {
    const result = await prisma.$transaction(async (tx) => {
      const event = await tx.event.create({
        data: {
          type,
          title: title.trim(),
          description: description?.trim() ?? null,
          location: location?.trim() ?? null,
          color: color ?? null,
          semesterId: semesterId ?? null,
          courseId: courseId ?? null,
          sectionId: sectionId ?? null,
          startTime: parseDate(startTime),
          endTime: parseDate(endTime),
          createdBy: req.user.id,
        },
        include: eventInclude,
      });

      let reminder = null;
      if (event.startTime) {
        reminder = await upsertEventReminder(event, req.user.id, tx);
      }

      return { event, reminder };
    });

    return res.status(201).json(result);
  } catch (err) {
    console.error("createEvent error:", err);
    return res.status(500).json({ error: "Server error" });
  }
};

