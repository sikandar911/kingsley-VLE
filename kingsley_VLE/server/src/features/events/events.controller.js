/**
 * @swagger
 * /api/events/{id}:
 *   put:
 *     summary: Update an event (re-syncs the auto-generated calendar reminder)
 *     tags: [Events]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema: { type: string }
 *     requestBody:
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               type:
 *                 type: string
 *                 enum: [institution, course, section]
 *               title: { type: string }
 *               description: { type: string }
 *               location: { type: string }
 *               color: { type: string }
 *               semesterId: { type: string }
 *               courseId: { type: string }
 *               sectionId: { type: string }
 *               startTime: { type: string, format: date-time }
 *               endTime: { type: string, format: date-time }
 *     responses:
 *       200:
 *         description: Updated event
 *       404:
 *         description: Not found
 */
export const updateEvent = async (req, res) => {
  const { id } = req.params;
  console.log("[UPDATE EVENT] Event ID:", id);
  console.log(
    "[UPDATE EVENT] Received request body:",
    JSON.stringify(req.body, null, 2),
  );

  const {
    type,
    title,
    description,
    location,
    color,
    semesterId,
    courseId,
    sectionId,
    startTime,
    endTime,
  } = req.body;

  console.log("[UPDATE EVENT] Parsed fields:", {
    type,
    title,
    description,
    location,
    color,
    semesterId,
    courseId,
    sectionId,
    startTime,
    endTime,
  });

  try {
    const existing = await prisma.event.findUnique({ where: { id } });
    if (!existing) return res.status(404).json({ error: "Event not found" });

    const result = await prisma.$transaction(async (tx) => {
      const data = {};
      if (type !== undefined) {
        const validTypes = ["institution", "course", "section"];
        if (!validTypes.includes(type)) {
          throw new Error(`type must be one of: ${validTypes.join(", ")}`);
        }
        data.type = type;
      }
      if (title !== undefined) data.title = title.trim();
      if (description !== undefined)
        data.description = description?.trim() ?? null;
      if (location !== undefined) data.location = location?.trim() ?? null;
      if (color !== undefined) data.color = color;
      if (semesterId !== undefined) data.semesterId = semesterId ?? null;
      if (courseId !== undefined) data.courseId = courseId ?? null;
      if (sectionId !== undefined) data.sectionId = sectionId ?? null;
      if (startTime !== undefined) data.startTime = parseDate(startTime);
      if (endTime !== undefined) data.endTime = parseDate(endTime);

      const event = await tx.event.update({
        where: { id },
        data,
        include: eventInclude,
      });

      // Re-sync auto reminder when startTime changes
      if (startTime !== undefined) {
        await deleteEventReminders(id, tx);
        if (event.startTime) {
          await upsertEventReminder(event, existing.createdBy, tx);
        }
      } else if (title !== undefined) {
        // Keep date in sync but update the name
        await tx.calendarReminder.updateMany({
          where: { eventId: id, isAutoGenerated: true },
          data: { name: event.title },
        });
      }

      return event;
    });

    return res.json(result);
  } catch (err) {
    console.error("updateEvent error:", err);
    return res.status(500).json({ error: "Server error" });
  }
};

