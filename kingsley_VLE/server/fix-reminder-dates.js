/**
 * fix-reminder-dates.js
 *
 * One-time script to fix CalendarReminder records whose stored `date` field
 * is wrong due to the old timezone-local `setHours(0,0,0,0)` bug.
 *
 * For assignment reminders: re-derives the correct UTC midnight date from
 * the linked assignment's dueDate.
 *
 * For event reminders: re-derives the correct UTC midnight date from the
 * linked event's startTime.
 *
 * Run from the server directory:
 *   node fix-reminder-dates.js
 *
 * Safe to run multiple times (only updates records where the stored date
 * differs from the correct UTC date).
 */

import prisma from "./src/config/prisma.js";

async function fixReminderDates() {
  console.log("Fetching all auto-generated reminders...");

  const reminders = await prisma.calendarReminder.findMany({
    where: { isAutoGenerated: true },
    include: {
      assignment: { select: { id: true, dueDate: true } },
      event: { select: { id: true, startTime: true } },
    },
  });

  console.log(`Found ${reminders.length} auto-generated reminders.`);

  let fixed = 0;
  let skipped = 0;

  for (const reminder of reminders) {
    let correctDate = null;

    if (reminder.type === "assignment" && reminder.assignment?.dueDate) {
      const d = new Date(reminder.assignment.dueDate);
      correctDate = new Date(
        Date.UTC(d.getUTCFullYear(), d.getUTCMonth(), d.getUTCDate()),
      );
    } else if (reminder.type === "event" && reminder.event?.startTime) {
      const d = new Date(reminder.event.startTime);
      correctDate = new Date(
        Date.UTC(d.getUTCFullYear(), d.getUTCMonth(), d.getUTCDate()),
      );
    }

    if (!correctDate) {
      skipped++;
      continue;
    }

    // Compare stored date (as UTC midnight) with correct date
    const stored = new Date(reminder.date);
    const storedUTC = new Date(
      Date.UTC(
        stored.getUTCFullYear(),
        stored.getUTCMonth(),
        stored.getUTCDate(),
      ),
    );

    if (storedUTC.getTime() !== correctDate.getTime()) {
      await prisma.calendarReminder.update({
        where: { id: reminder.id },
        data: { date: correctDate },
      });
      console.log(
        `  Fixed reminder ${reminder.id} (${reminder.name}): ` +
          `${storedUTC.toISOString().slice(0, 10)} → ${correctDate.toISOString().slice(0, 10)}`,
      );
      fixed++;
    } else {
      skipped++;
    }
  }

  console.log(`\nDone. Fixed: ${fixed}, Already correct: ${skipped}`);
  await prisma.$disconnect();
}

fixReminderDates().catch((err) => {
  console.error(err);
  prisma.$disconnect();
  process.exit(1);
});
